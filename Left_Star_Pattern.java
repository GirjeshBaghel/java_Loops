public class Left_Star_Pattern {
	
	// this main method
	public static void main(String[] args) {
		
		// it is local variable
		int i,j;
		
		// it is for_loop for print a star pattern
		for(i=1;i<=5;i++ ) 
		{
			for(j=1;j<=i;j++ ) 
			{
				System.out.print("*" );
			}
			   // this line is used for braking the line
			   System.out.println();
		}

	}

}
